fun initCourseList(repo: Repository): List<Course> = runBlocking {
    if (repo.getCourses().isEmpty()) { // Check if the database is empty
        val courses = listOf(
            Course(name = "Spanish"),
            Course(name = "French"),
            Course(name = "Turkish"),
            Course(name = "Greek"),
            Course(name = "Chinese")
        )

        courses.forEach { course ->
            repo.insertCourse(course)
        }

        courses
    } else {
        emptyList()
    }
}

